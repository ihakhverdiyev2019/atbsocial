package atb.social.network.dto;

public class QuoteDTO {

    private String author;

    private String text;


    public QuoteDTO() {
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
