package atb.social.network.dto;

public class CounterDTO {

    private int hits;

    private int hosts;


    public CounterDTO() {
    }

    public int getHits() {
        return hits;
    }

    public void setHits(int hits) {
        this.hits = hits;
    }

    public int getHosts() {
        return hosts;
    }

    public void setHosts(int hosts) {
        this.hosts = hosts;
    }
}
